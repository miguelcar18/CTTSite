<h2>¿De donde extrae la información Javascript?</h2><br>

Javascript es un lenguaje de programación que funciona del lado del cliente, es decir al cargar una página, en la cache del navegador se almacena el código que contiene, aun si está en archivos independientes de la página (archivos JS).<br>
Al ejecutarla aunque sea sin conexión todas las funciones se ejecutan correctamente, a no ser que existan &quot;trampas&quot; en el código.<br>
Los códigos en esta página lo que hacen simplemente es escribir los datos que el navegador del usuario envía en los encabezados HTTP, al hacer la petición de la página.<br>
Dicha petición se hace a toda velocidad y no nos percatamos, al mostrarse el primer elemento significa que fue aceptada por el servidor.<br>
Ninguno de ellos es dañino en lo absoluto, pueden usarse en cualquier página o en un blog de cualquier servicio como Wordpress o Blogger.<br><br>

